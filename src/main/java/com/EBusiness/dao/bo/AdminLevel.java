package com.EBusiness.dao.bo;

public class AdminLevel {
    private Integer adminLevelId;

    private String adminLevelRole;

    private String adminLevelDesc;

    public Integer getAdminLevelId() {
        return adminLevelId;
    }

    public void setAdminLevelId(Integer adminLevelId) {
        this.adminLevelId = adminLevelId;
    }

    public String getAdminLevelRole() {
        return adminLevelRole;
    }

    public void setAdminLevelRole(String adminLevelRole) {
        this.adminLevelRole = adminLevelRole;
    }

    public String getAdminLevelDesc() {
        return adminLevelDesc;
    }

    public void setAdminLevelDesc(String adminLevelDesc) {
        this.adminLevelDesc = adminLevelDesc;
    }
}