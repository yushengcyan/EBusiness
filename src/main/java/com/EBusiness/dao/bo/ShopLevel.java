package com.EBusiness.dao.bo;

public class ShopLevel {
    private Integer shopLevelId;

    private String shopLevelName;

    private String shopLevelLow;

    private String shopLevelHigh;

    private String shopLevelDesc;

    private String shopLevelExt;

    public Integer getShopLevelId() {
        return shopLevelId;
    }

    public void setShopLevelId(Integer shopLevelId) {
        this.shopLevelId = shopLevelId;
    }

    public String getShopLevelName() {
        return shopLevelName;
    }

    public void setShopLevelName(String shopLevelName) {
        this.shopLevelName = shopLevelName;
    }

    public String getShopLevelLow() {
        return shopLevelLow;
    }

    public void setShopLevelLow(String shopLevelLow) {
        this.shopLevelLow = shopLevelLow;
    }

    public String getShopLevelHigh() {
        return shopLevelHigh;
    }

    public void setShopLevelHigh(String shopLevelHigh) {
        this.shopLevelHigh = shopLevelHigh;
    }

    public String getShopLevelDesc() {
        return shopLevelDesc;
    }

    public void setShopLevelDesc(String shopLevelDesc) {
        this.shopLevelDesc = shopLevelDesc;
    }

    public String getShopLevelExt() {
        return shopLevelExt;
    }

    public void setShopLevelExt(String shopLevelExt) {
        this.shopLevelExt = shopLevelExt;
    }
}